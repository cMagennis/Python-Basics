# Basic user input
# We can provide a prompt within the parenthesis
# Here we're directly assigning the input to a variable, user_name

user_name = input("Enter your name: ")

print("Hello " + user_name + "!")

# We can also prompt the user to enter multiple pieces of information

user_age = input("Enter your age: ")

print("So your name is " + user_name + " and you are " + user_age + " years old.")